    //---->>>Metodo Insertar Direccion.

    public void Direcion() {
        String Sql = "Insert Into direccion(,departamento,Ciudad,Carrera,Calle,Codigo_postal,Localidad, Barrio, empleados_Cedula) Values(?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement ps = com.prepareStatement(Sql);
            ps.setString(2, txt_deprtamento.getText());
            ps.setString(3, txt_ciudad.getText());
            ps.setString(4, txt_carrera.getText());
            ps.setString(5, txt_calle.getText());
            ps.setString(6, txt_codigopostal.getText());
            ps.setString(7, txt_localida.getText());
            ps.setString(8, txt_barrio.getText());
           
            ps.execute();

            JOptionPane.showMessageDialog(null, "Registro exitoso");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Direc " + e.getMessage());

        }

    }
