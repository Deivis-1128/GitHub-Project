    //----->>>Metodo Mostrar Direccion.
    public void MostrarDireccion() {
        String[] Titulos = {"Departamento", "Ciudad", "Localidad", "Carrera", "Calle", "Barrio", "Cod postal", "Empleado"};
        String[] Registros = new String[8];
        DefaultTableModel modelo = new DefaultTableModel(null, Titulos);
        String Sql = "Select* From direcciones";
        try {

            try {

                Statement st = com.createStatement();
                ResultSet rs = st.executeQuery(Sql);

                while (rs.next()) {

                    Registros[0] = rs.getString("departamento");
                    Registros[1] = rs.getString("Ciudad");
                    Registros[2] = rs.getString("Localidad");
                    Registros[3] = rs.getString("Carrera");
                    Registros[4] = rs.getString("Calle");
                    Registros[5] = rs.getString("Barrio");
                    Registros[6] = rs.getString("Codigo_postal");
                    Registros[7] = rs.getString("Cedula");

                    modelo.addRow(Registros);

                }
                tbl_direccion.setModel(modelo);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Nose Pueden Cargar los Datosd " + e.getMessage());
            }

        } catch (Exception e) {
        }

    }
